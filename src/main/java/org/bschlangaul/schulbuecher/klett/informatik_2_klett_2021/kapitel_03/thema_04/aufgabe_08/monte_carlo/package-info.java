
/**
 * <p>
 * Project &quot;figures&quot; Authors: Michael Kölling and David J. Barnes
 * </p>
 * <p>
 * This project is part of the material for the book
 * </p>
 *
 * <pre>
 * <code>Objects First with Java - A Practical Introduction using BlueJ
 * Sixth edition
 * David J. Barnes and Michael Kölling
 * Pearson Education, 2016</code>
 * </pre>
 * <p>
 * It is discussed in chapter 1.
 * </p>
 * <p>
 * This is a very simple project to demonstrate some characteristics of objects.
 * </p>
 * <p>
 * You can create various shapes, and you will see, if you do, that those shapes
 * are drawn on screen (in a window that we call the &quot;canvas&quot;).
 * </p>
 * <p>
 * You can then manipulate these objects: change their position, size and
 * colour. Try it out: create a few different squares, triangles and circles.
 * </p>
 * <p>
 * This project is designed as a first example of object-oriented programming.
 * It illustrates a number of concepts:
 * </p>
 * <ul>
 * <li>a Java project (application) is a collection of classes</li>
 * <li>objects can be created from classes</li>
 * <li>from any one class, many objects may be created</li>
 * <li>objects have operations (methods)</li>
 * <li>operations can have parameters</li>
 * <li>parameters have types (at least String and int)</li>
 * <li>objects hold data (fields)</li>
 * <li>the operations and fields are common to all objects</li>
 * <li>the values stored in the fields can be different for each object</li>
 * </ul>
 * <p>
 * The project also demonstrates
 * </p>
 * <ul>
 * <li>BlueJ object creation</li>
 * <li>interactive method invocation</li>
 * <li>parameter passing</li>
 * </ul>
 * <p>
 * A good second project to look at after this is &quot;picture&quot;, which
 * adds a class to those ones in this project. That class (named
 * &quot;Picture&quot;) uses the shapes to draw a picture. It can be used to
 * experiment with coding.
 * </p>
 */
